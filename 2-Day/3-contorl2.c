#include "stdio.h"

int main(){

    int i=0;
    int count=0;
    printf("This is looping lesson!\n");

    for(i=500; i<=1000; i=i+2 ){

        printf("Looping Counting:=%d\n",i);
        printf("Count =%d\n",count);
        count = count+2;

    }


}